/**
 * 判断一个值是否为对象
 *
 * @param thing 要进行测试的值
 *
 * @returns 如果值是一个对象（Object），则返回 true，否则返回 false
 */
const isObject = (thing: any): thing is Object => {
  return thing !== null && typeof thing === 'object'
};

/**
 * 确定值是否是普通对象
 *
 * @param val 要进行测试的值
 *
 * @returns {boolean} 如果值是一个简单对象，则返回 true，否则返回 false
 */
const isPlainObject = (val: any): val is Object => {
  if (kindOf(val) !== 'object') {
    return false;
  }

  const prototype = getPrototypeOf(val);
  return (prototype === null || prototype === Object.prototype || Object.getPrototypeOf(prototype) === null) && !(Symbol.toStringTag in val) && !(Symbol.iterator in val);
}

/**
 * 判断一个字符串是否以指定字符串的字符结尾
 *
 * @param str
 * @param searchString
 * @param [position= 0]
 */
const endsWith = (str: string, searchString: string, position: number = 0) => {
  str = String(str);
  if (position === undefined || position > str.length) {
    position = str.length;
  }
  position -= searchString.length;
  const lastIndex = str.indexOf(searchString, position);
  return lastIndex !== -1 && lastIndex === position;
}

